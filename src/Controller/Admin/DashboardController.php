    public function configureDashboard(): Dashboard
    {
        return Dashboard::new()
            ->setTitle('WorkDashboard')

            // by default EasyAdmin displays a black square as its default favicon;
            // use this method to display a custom favicon: the given path is passed
            // "as is" to the Twig asset() function:
            // <link rel="shortcut icon" href="{{ asset('...') }}">
            ->setFaviconPath('favicon.svg')

            // set this option if you prefer the page content to span the entire
            // browser width, instead of the default design which sets a max width
            ->renderContentMaximized()

            // set this option if you prefer the sidebar (which contains the main menu)
            // to be displayed as a narrow column instead of the default expanded design
            ->renderSidebarMinimized()

            // by default, users can select between a "light" and "dark" mode for the
            // backend interface. Call this method if you prefer to disable the "dark"
            // mode for any reason (e.g. if your interface customizations are not ready for it)
            ->disableDarkMode()

            // by default, all backend URLs are generated as absolute URLs. If you
            // need to generate relative URLs instead, call this method
            ->generateRelativeUrls()
            ;
    }

    public function configureMenuItems(): iterable
    {
        yield MenuItem::linkToDashboard('Dashboard', 'fa fa-home');
        yield MenuItem::linkToCrud('Assignments', 'fa fa-thumbtack', Assignments::class);
        yield MenuItem::linkToCrud('Ledger', 'fa fa-file-contract', Contracts::class);
        yield MenuItem::linkToCrud('Engineers', 'fa fa-users', Engineers::class);
        yield MenuItem::linkToCrud('Projects', 'fa fa-list-check', Projects::class);
        yield MenuItem::linkToCrud('Users', 'fa fa-user', User::class);
        yield MenuItem::linkToCrud('Roles', 'fa fa-usd', Roles::class);
        yield MenuItem::linkToCrud('Assigned Roles', 'fa fa-usd', AssignedRoles::class);
    }
